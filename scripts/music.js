document.addEventListener("DOMContentLoaded", function () {
    // Play the background music when the DOM is fully loaded
    var audio = document.getElementById("backgroundMusic");
    audio.play();
    audio.volume=0.2;

    // Save the current time of the audio when navigating to a new page
    document.querySelectorAll("a").forEach(function (link) {
        link.addEventListener("click", function () {
            localStorage.setItem("audioTime", audio.currentTime);
        });
    });

    // Restore the saved time and play the audio when the new page is loaded
    var savedTime = localStorage.getItem("audioTime");
    if (savedTime) {
        audio.currentTime = savedTime;
    }
});
